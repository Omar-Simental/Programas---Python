# Combinar dos palabras con el mismo numero de caracteres

palabra1 = 'Alegre'
palabra2 = 'Triste'


print(f' La palabra 1 es : {palabra1} y tiene {len(palabra1)} digitos')
print(f' La palabra 2 es : {palabra2} y tiene {len(palabra2)} digitos')

combinadas = palabra1 + palabra2
print(f' Las palabras combinadas son : {combinadas} y tiene {len(combinadas)} digitos')