# Mostrar el numero equivalente en romano

print('Mostrando el numero equivalente en romano...')
numero = int(input('Dame un numero... '))
if numero == 1:
    print(f'El numero {numero} equivale a I en romano ')
elif numero == 2:
    print(f'El numero {numero} equivale a II en romano ')
elif numero == 3:
    print(f'El numero {numero} equivale a III en romano ')
elif numero == 4:
    print(f'El numero {numero} equivale a IV en romano ')
elif numero == 5:
    print(f'El numero {numero} equivale a V en romano ')
elif numero == 6:
    print(f'El numero {numero} equivale a VI en romano ')
elif numero == 7:
    print(f'El numero {numero} equivale a VII en romano ')
elif numero == 8:
    print(f'El numero {numero} equivale a VIII en romano ')
elif numero == 9:
    print(f'El numero {numero} equivale a IX en romano ')
elif numero == 10:
    print(f'El numero {numero} equivale a X en romano ')
else:
    print('Error')