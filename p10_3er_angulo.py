# Calcular el tercer angulo de un triangulo

print('Calculando el tercer angulo de un triangulo... ')
angulo1 = float(input('Dame el valor del angulo 1 '))
angulo2 = float(input('Dame el valor del angulo 2 '))
angulo3 = 180 - (angulo1 + angulo2)

print(f'Para un triangulo con angulo1: {angulo1} y angulo2: {angulo2} el valor de su tercer angulo es: {angulo3}')
